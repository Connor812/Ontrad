                <div id="story" class="collapse">
                    <p>Since the late 1970s I have been seeking out, learning, performing and teaching the
                        grassroots music
                        of my home province of Ontario, Canada. I have continued this work up to the present,
                        and
                        while not
                        conducted in any systematic manner my collecting has over the years created a sizeable
                        private
                        database. It includes recordings of, and written music for traditional/historical songs,
                        instrumental music, written dance instructions, and many early references to music
                        performances and
                        dance taken from published and private journals, diaries, posters, playbills, newspaper
                        articles,
                        and other writings.
                    </p>
                    <p>This collection is by no means comprehensive, or representative of anything beyond my own
                        interests
                        and musical abilities. Some has survived in the oral tradition, some in printed sources.
                        It
                        is
                        largely English language and for the most part comes from Ontario’s settler Scots,
                        Irish,
                        English
                        and African-American traditions. It also includes a small amount French and Gaelic
                        material.
                        Some of
                        it was acquired through personal contact and interaction with tradition-bearers, some
                        was
                        found in
                        print and other published sources. It is what it is - and there is quite a lot of it.
                    </p>
                    <p>Over the last four decades countless people have come up to me at my performances and
                        handed
                        me
                        things that they (rightly) thought might interest me – instruments, books, binders of
                        sheet
                        music,
                        song manuscripts, cassettes, 78s, lists of songs their grandmother knew. More recently
                        these
                        offerings have included PDFs and other digital gifts.
                    </p>
                    <p>The ONTRAD Library is intended to make this material easily available to anyone who might
                        be
                        interested in learning about or performing these songs and tunes from Ontario’s past. It
                        is
                        loosely
                        based on a similar website about the folk music of Gloucestershire, UK that was pointed
                        out
                        to me
                        some years ago by Brad McEwan. Thanks to them and him for the inspiration!
                    </p>
                    <p>The ONTRAD Library was originally launched on Groundhog Day 2017. In late 2022 it was
                        ruthlessly
                        cyber-attacked by internet pirates and sank with all hands. It has been rebuilt and
                        launched
                        again with
                        the generous assistance of David Bowen in 2023.
                    </p>
                    <p>If there is any material in this collection on which feel you have some personal claim,
                        and
                        which you
                        would rather not be included in the library, please contact me by e-mail. Likewise for
                        any
                        errors or
                        omissions you may notice. Thanks!
                    </p>
